//=============================================================================
//函数名称: delay_us
//功能概要:每次定时1us
//参数名称:无
//函数返回:无
//注意   :无
//=============================================================================
void Delay_us(u16 nus)
{
  u8 i;	
  while(nus--)
  {
  	i=5;
	while(i--);
  }
}
//=============================================================================
//函数名称: delay_ms
//功能概要:每次定时1ms
//参数名称:无
//函数返回:无
//注意   :无
//=============================================================================
void Delay_ms( uint16_t time_ms )
{
  uint16_t i,j;
  for( i=0;i<time_ms;i++ )
  {
    for( j=0;j<10309;j++ );//大约1ms
  }
}



